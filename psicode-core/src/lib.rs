//! psicode-core: общее ядро оптического канала «монитор -> камера».
//!
//! Сейчас реализован калибровочный профиль: 200-битный код (40 символов,
//! 8 групп по 5) для ручного обратного канала (телефон показывает, человек
//! вводит на передатчике).
//!
//! Слои:
//! - [`gf32`]    — арифметика GF(32)
//! - [`rs`]      — два перемежённых Рид-Соломона RS(20,12): любые 4 опечатки,
//!                 до 8 при равномерном распределении
//! - [`base32`]  — Crockford Base32 с терпимым парсером
//! - [`bits`]    — битовая упаковка payload (120 бит на u128)
//! - [`profile`] — [`profile::CalibProfile`]: поля, CRC-8, encode/decode строки

pub mod base32;
pub mod bits;
pub mod gf32;
pub mod profile;
pub mod rs;

pub use profile::{CalibProfile, ChromaMode, ProfileError};
